package Wynn_Days016;
public class OperatorAritmatika {
    public static void main(String[] args) {
        int a = 20;
        int b = 5;

        int kali = a * b; 
        System.out.println("Hasil perkalian = " + kali);

        int bagi = a / b; 
        System.out.println("Hasil pembagian = " + bagi);
    }
}
