package day5;

public class Day5 {

    public static void main(String[] args) {
       // byte  -> untuk bilangan bulat dengan ukuran 8 bit
       byte a = 12;
       // short  -> untuk bilangan bulat dengan ukuran 16 bit
       short b = 21; 
       // int -> untuk bilangan bulat dengan ukuran 32 bit
       int c = 123456;
       // long  -> untuk bilangan bulat dengan ukuran 64 bit
       long d = 12345678910;
       
        System.out.println(a);
        System.out.println(b);
        System.out.println(c);
        System.out.println(d);
    }
    
}
